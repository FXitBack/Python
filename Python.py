# print (wsup)
# **- Возведение в степень
# %- Деление по модулю
# // - целочисленное деление с отбрасыванием дробной части
print(27 % 3)
print(27 // 3)
print(27 + 3)
print(27 - 3)
print(27 * 3)
# int() - целочисленый тип данных, целые числа
# "" -  str() строки
# float - вещественные числа с плавающей точкой
my_name = "Vladimir "
my_secondname = "Ivanov"
my_age = 29
print(my_name +" "+ my_secondname)
print(my_name +" "+ str(my_age) +" "+ my_secondname)
print(f"{my_name} мой возраст {my_age}")
print(f"{my_name} {my_name} {my_name} {my_name} {my_name}")
print(f"{my_name*5 }")
